/*
Escribir todos los numeros del 100 al 0 de 7 en 7.
 */
package CiclosEjercicios;

public class EJciclos5 {

    public static void main(String[] args) {
        for (int i = 100; i >= 0; i-=7) {//Si no se cumple la condicion, se termina el proceso.
            System.out.println(i);
        }
    }
    
}
